package gherkin;

public class Kalkulator {
    // KALKULATOR

    // Penjumlahan
    public int penjumlahan(int a, int b) {
        return a + b;
    }
    // Penjumlahan

    //penjumlahan salah
    public int pennjumlahan(int a, int b) {
        return a * b;
    }
    //penjumlahan salah

    // pengurangan
    public int pengurangan(int a, int b) {
        return a - b;
    }
    // pengurangan

    //perkalian
    public int perkalian(int a, int b) {
        return a * b;
    }
    //perkalian

    //pembagian
    public int pembagian(int a, int b) {
        return a / b;
    }
    //pembagian

}